 * @method UserQuery leftJoinCode($relationAlias = null) Adds a LEFT JOIN clause to the query using the Code relation
 * @method UserQuery rightJoinCode($relationAlias = null) Adds a RIGHT JOIN clause to the query using the Code relation
 * @method UserQuery innerJoinCode($relationAlias = null) Adds a INNER JOIN clause to the query using the Code relation
